// colors ------------------------------------------------------------------------------------------
export function green(s: string): string { return "\e[32m" + s + "\e[0m" }
export function grey(s: string): string { return "\e[90m" +  s +  "\e[0m" }
export function yellow(s: string): string { return "\e[33m" +  s +  "\e[0m" }
export function red(s: string): string { return "\e[31m" +  s +  "\e[0m" }